  /**
   * 指定タイプの空Stateオブジェクトを返す。
   * 各プロパティを空配列で初期化。
   */
  getDefaultState(type: "global" | "local"): GlobalState | LocalState {
    const result: Record<string, []> = {};
    for (const key of Object.keys(this.stateProperties[type])) {
      result[key] = [];
    }
    return result;
  }

  /**
   * stateプロパティの説明文テキストを返す。
   * プロンプト内で【state構造解説】として使われる。
   */
  getStateDefinition(type: "global" | "local"): string {
    const lines: string[] = [];
    for (const [key, def] of Object.entries(this.stateProperties[type])) {
      lines.push(`【${key}】${def.description}`);
      if (def.howTo) {
        lines.push(`  記載方針: ${def.howTo.trim()}`);
      }
    }
    return lines.join("\n");
  }



  /**
   * State構築方法のガイドテキスト＋JSONスキーマを返す。
   * Agent が _createInitialState / _updateStateWithToolResult で使う。
   */
  getHowtoBuild(): string {
    const allProps = [
      ...Object.entries(this.stateProperties.global),
      ...Object.entries(this.stateProperties.local),
    ];

    const lines = [
      "【各stateプロパティの記載方針】",
      ...allProps
        .filter(([, def]) => def.howTo)
        .map(([key, def]) => `■ ${key}\n${def.howTo!.trim()}`),
      "",
      "【出力JSONスキーマ】",
      "各プロパティは { id: string, text: string }[] 形式の配列で出力すること。",
      "例:",
      JSON.stringify(
        {
          goals: [{ id: "goal-1", text: "目標のテキスト" }],
          tasks: [{ id: "task-1", text: "【次に実行】タスクのテキスト" }],
        },
        null,
        2
      ),
    ];
    return lines.join("\n");
  }
